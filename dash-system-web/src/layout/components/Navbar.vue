<template>
  <div class="navbar">
    <hamburger :is-active="sidebar.opened" class="hamburger-container" @toggleClick="toggleSideBar" />

    <breadcrumb class="breadcrumb-container" />

    <div class="right-menu">
      <template v-if="device!=='mobile'">
        <screenfull class="right-menu-item hover-effect" />
      </template>

      <el-dropdown class="avatar-container right-menu-item hover-effect" trigger="click">
        <div class="avatar-wrapper" style="height:50px;margin-top: 0px;line-height:50px;position: relative;vertical-align: middle;">
          <img :title="name" style="margin-top: 5px;" src="https://wpimg.wallstcn.com/0e03b7da-db9e-4819-ba10-9016ddfdaed3" class="user-avatar">
        </div>
        <el-dropdown-menu slot="dropdown">
          <router-link to="/home">
            <el-dropdown-item>
              主页
            </el-dropdown-item>
          </router-link>
          <el-dropdown-item divided @click.native="updatePasswordHandle()">
            更改密码
          </el-dropdown-item>
          <el-dropdown-item divided @click.native="layOutSettings()">
            系统布局配置
          </el-dropdown-item>
          <el-dropdown-item divided>
            <span style="display:block;" @click="logout">退出登录</span>
          </el-dropdown-item>
        </el-dropdown-menu>
      </el-dropdown>
    </div>
    <update-password v-if="updatePassowrdVisible" ref="updatePassowrd" />
  </div>

</template>

<script>
import { mapGetters } from 'vuex'
import Breadcrumb from '@/components/Breadcrumb'
import Hamburger from '@/components/Hamburger'
import Screenfull from '@/components/Screenfull'
import UpdatePassword from './UpdatePassword'

export default {
  components: {
    Breadcrumb,
    Hamburger,
    Screenfull,
    UpdatePassword
  },
  data() {
    return {
      updatePassowrdVisible: false
    }
  },
  computed: {
    ...mapGetters([
      'sidebar',
      'name',
      'avatar',
      'device'
    ])
  },
  methods: {
    layOutSettings() {
      this.$store.dispatch('settings/setShowRightPanel', true)
    },
    // 修改密码
    updatePasswordHandle() {
      this.updatePassowrdVisible = true
      this.$nextTick(() => {
        this.$refs.updatePassowrd.init()
      })
    },
    toggleSideBar() {
      this.$store.dispatch('app/toggleSideBar')
    },
    async logout() {
      await this.$store.dispatch('user/logout')
      await this.$store.dispatch('permission/setDynamicRoutes', false)
      this.$router.push(`/login?redirect=${this.$route.fullPath}`)
    }
  }
}
</script>

